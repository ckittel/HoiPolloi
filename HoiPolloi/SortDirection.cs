namespace HoiPolloi
{
    /// <summary>
    /// Specifies the direction in which a list can be sorted.
    /// </summary>
    public enum SortDirection
    {
        /// <summary>
        /// Sort from smallest to largest.  For example, from A to Z or 0 to 100.
        /// </summary>
        Ascending = 0,

        /// <summary>
        /// Sort from largest to smallest.  For example, from Z to A or 100 to 0.
        /// </summary>
        Descending
    }
}
